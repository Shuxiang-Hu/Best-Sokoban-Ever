package object;
/**
 * Represents a floor, which is subclass of GameObject
 * with a symbol character ' '
 * @author Shuxiang Hu
 */
public class GameFloor extends GameObject {
    /**
     * Constructs a GameFloor object with a
     * character symbol ' '
     */
    public GameFloor() {
        setCharSymbol(' ');;
    }
}
