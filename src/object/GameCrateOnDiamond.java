package object;
/**
 * Represents a crate on a diamond, which is subclass of GameObject
 * with a symbol character 'O'
 * @author Shuxiang Hu
 */
public class GameCrateOnDiamond extends GameObject {
    /**
     * Constructs a GameCrateOnDiamond object with a
     * character symbol 'O'
     */
    public GameCrateOnDiamond() {
        setCharSymbol('O');
    }
}
