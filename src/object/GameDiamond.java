package object;
/**
 * Represents a diamond, which is subclass of GameObject
 * with a symbol character 'D'
 * @author Shuxiang Hu
 */
public class GameDiamond extends GameObject {
    /**
     * Constructs a GameDiamond object with a
     * character symbol 'D'
     */
    public GameDiamond() {
        setCharSymbol('D');;
    }
}
