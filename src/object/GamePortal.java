package object;


/**
 * Represents a portal, which is subclass of GameObject
 * with a symbol character 'P'
 * @author Shuxiang Hu
 */
public class GamePortal extends GameObject {
    /**
     * Constructs a GamePortal object with a
     * character symbol 'P'
     */
    public GamePortal() {
        setCharSymbol('P');
    }
}
