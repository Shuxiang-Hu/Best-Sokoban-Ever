package object;


/**
 * Represents the exit of a portal, which is subclass of GameObject
 * with a symbol character 'E'
 * @author Shuxiang Hu
 */
public class GamePortalExit extends GameObject {
    /**
     * Constructs a GamePortalExit object with a
     * character symbol 'E'
     */
    public GamePortalExit() {
        setCharSymbol('E');
    }
}
